const mongoose = require('mongoose')

let Transaction = new mongoose.Schema({
  amount: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    required: true,
    default: Date.now,
  },
})

module.exports = mongoose.model('Transaction', Transaction)
